#!/bin/bash
# Скрипт для запуска бота в фоновом режиме

# Переходим в директорию с ботом
cd "$(dirname "$0")"

# Активируем виртуальное окружение
source venv/bin/activate

# Запускаем бота в фоновом режиме
nohup python3 spy.py > spy_output.txt 2>&1 &

# Выводим PID процесса
echo "Бот запущен в фоновом режиме. PID: $!"
echo "Вывод записывается в файл spy_output.txt"
